const createUsePageDataHook = (): string => `import { useEffect, useState } from 'react';

const BASE_URL = import.meta.env.VITE_API_BASE_URL ?? 'http://localhost:3000';

interface UsePageDataResult<T> {
  data: T | null;
  loading: boolean;
  error: string | null;
}

export function usePageData<T = Record<string, unknown>>(
  pageSlug: string
): UsePageDataResult<T> {
  const [data, setData] = useState<T | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    let cancelled = false;
    setLoading(true);
    setError(null);

    fetch(\`\${BASE_URL}/api/page-data/\${pageSlug}\`)
      .then((res) => {
        if (!res.ok) throw new Error(\`Failed to load page data: \${res.status}\`);
        return res.json();
      })
      .then((result: T) => {
        if (!cancelled) {
          setData(result);
          setLoading(false);
        }
      })
      .catch((err: Error) => {
        if (!cancelled) {
          setError(err.message);
          setLoading(false);
        }
      });

    return () => {
      cancelled = true;
    };
  }, [pageSlug]);

  return { data, loading, error };
}
`;

