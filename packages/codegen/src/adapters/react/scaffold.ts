export const createRoutesFile = (
  pages: Array<{ componentName: string; page: Page }>
): string => {
  console.info(`${LOG_PREFIX} Generating routes for ${pages.length} page(s)`);

  if (pages.length === 0) {
    return `import type { RouteObject } from 'react-router-dom';

export const routes: RouteObject[] = [
  {
    path: '/',
    element: <div style={{ padding: '2rem', textAlign: 'center' }}>No pages configured yet.</div>
  }
];
`;
  }

  const imports = pages
    .map(
      ({ componentName }) =>
        `import { ${componentName} } from './pages/${componentName}';`
    )
    .join('\n');

  const routeEntries = pages
    .map(
      ({ componentName, page }) =>
        `  {\n    path: '${page.route}',\n    element: <${componentName} />\n  }`
    )
    .join(',\n');

  return `import type { RouteObject } from 'react-router-dom';
${imports}

export const routes: RouteObject[] = [
${routeEntries}
];
`;
};

