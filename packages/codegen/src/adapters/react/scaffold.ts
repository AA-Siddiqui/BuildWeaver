export const createScaffoldFiles = (project: ProjectIR): GeneratedFile[] => {
  const pageCount = project.pages.length;
  console.info(`${LOG_PREFIX} Creating scaffold files for project "${project.metadata.name}" with ${pageCount} page(s)`);

  return [
    { path: 'package.json', contents: createPackageJson(project) },
    { path: 'vite.config.ts', contents: createViteConfig() },
    { path: 'tsconfig.json', contents: createTsconfig() },
    { path: 'tsconfig.node.json', contents: createTsconfigNode() },
    { path: 'index.html', contents: createIndexHtml(project.metadata.name) },
    { path: '.env.example', contents: createEnvExample() },
    { path: 'src/main.tsx', contents: createMainFile() },
    { path: 'src/hooks/usePageData.ts', contents: createUsePageDataHook() },
    { path: 'src/styles/global.css', contents: createGlobalCss() },
    { path: 'README.md', contents: createReadme(project, pageCount) }
  ];
};

