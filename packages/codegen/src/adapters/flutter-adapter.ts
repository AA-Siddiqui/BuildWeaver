const escapeDartString = (value: string): string =>
  value.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\r?\n/g, '\\n');

const toPackageName = (value: string): string => {
  const normalized = value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9_]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/(^_|_$)+/g, '');

  const withoutLeadingDigits = normalized.replace(/^[^a-z]+/, '');
  return withoutLeadingDigits || 'buildweaver_flutter_app';
};

const normalizeRoute = (route: string): string => {
  const trimmed = route.trim();
  if (!trimmed) {
    return '/';
  }
  return trimmed.startsWith('/') ? trimmed : `/${trimmed}`;
};

const routeToSlug = (route: string): string =>
  route
    .replace(/^\/+/, '')
    .replace(/\/+$/, '') || 'index';

const toClassName = (value: string): string =>
  value
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim()
    .split(' ')
    .filter(Boolean)
    .map((segment) => segment.charAt(0).toUpperCase() + segment.slice(1))
    .join('') || 'GeneratedPage';

const toFileSegment = (value: string): string =>
  value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/(^_|_$)+/g, '') || 'generated_page';

const encodeBase64 = (value: string): string => {
  const bytes = new TextEncoder().encode(value);
  const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/';
  let result = '';

  for (let index = 0; index < bytes.length; index += 3) {
    const first = bytes[index];
    const hasSecond = index + 1 < bytes.length;
    const hasThird = index + 2 < bytes.length;
    const second = hasSecond ? bytes[index + 1] : 0;
    const third = hasThird ? bytes[index + 2] : 0;

    const combined = (first << 16) | (second << 8) | third;
    const charOne = alphabet[(combined >> 18) & 0x3f];
    const charTwo = alphabet[(combined >> 12) & 0x3f];
    const charThree = hasSecond ? alphabet[(combined >> 6) & 0x3f] : '=';
    const charFour = hasThird ? alphabet[combined & 0x3f] : '=';

    result += `${charOne}${charTwo}${charThree}${charFour}`;
  }

  return result;
};

const asObject = (value: unknown): Record<string, unknown> | null => {
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    return null;
  }
  return value as Record<string, unknown>;
};

