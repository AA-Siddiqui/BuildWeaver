const coerceBuilderState = (
  value: unknown,
  pageName: string
): Record<string, unknown> => {
  const fallback = createFallbackBuilderState(pageName);
  const candidate = asObject(value);
  if (!candidate) {
    return fallback;
  }

  const root = asObject(candidate.root) ?? fallback.root;
  const content = Array.isArray(candidate.content)
    ? candidate.content
    : (fallback.content as unknown[]);

  const zones = asObject(candidate.zones);

  return {
    root,
    content,
    ...(zones ? { zones } : {})
  };
};

