const createProjectManifestFile = (
  project: ProjectIR,
  pages: FlutterPageModel[]
): string => {
  const pageEntries = pages
    .map((page) => {
      const dynamicInputs = page.dynamicInputs
        .map(
          (input) => `        GeneratedDynamicInput(
          id: '${escapeDartString(input.id)}',
          label: '${escapeDartString(input.label)}',
          dataType: '${escapeDartString(input.dataType)}',
          listItemType: ${
            input.listItemType
              ? `'${escapeDartString(input.listItemType)}'`
              : 'null'
          },
        )`
        )
        .join(',\n');

      return `    GeneratedPageSpec(
      id: '${escapeDartString(page.id)}',
      name: '${escapeDartString(page.name)}',
      route: '${escapeDartString(page.route)}',
      slug: '${escapeDartString(page.slug)}',
      builderStateBase64: '${page.builderStateBase64}',
      dynamicInputs: [
${dynamicInputs}
      ],
    )`;
    })
    .join(',\n');

  return `import '../models/generated_models.dart';

class GeneratedProjectManifest {
  static const String projectName = '${escapeDartString(project.metadata.name)}';

  static const List<GeneratedPageSpec> pages = [
${pageEntries}
  ];

  static String get initialRoute => pages.isNotEmpty ? pages.first.route : '/';

  static GeneratedPageSpec pageById(String id) {
    for (final page in pages) {
      if (page.id == id) {
        return page;
      }
    }
    return pages.first;
  }
}
`;
};

