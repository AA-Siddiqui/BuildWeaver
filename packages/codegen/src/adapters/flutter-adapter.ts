const reserveUnique = (base: string, seen: Map<string, number>): string => {
  const normalized = base || 'GeneratedValue';
  const count = seen.get(normalized) ?? 0;
  seen.set(normalized, count + 1);
  return count === 0 ? normalized : `${normalized}${count + 1}`;
};

const buildPageModels = (project: ProjectIR): FlutterPageModel[] => {
  const pages = ensurePages(project);
  const seenClassNames = new Map<string, number>();
  const seenFileNames = new Map<string, number>();

  return pages.map((page, index) => {
    const route = normalizeRoute(page.route);
    const slug = routeToSlug(route);

    const baseClassName = `${toClassName(page.name || slug || `Page${index + 1}`)}Page`;
    const className = reserveUnique(baseClassName, seenClassNames);

    const baseFileName = `${toFileSegment(slug || page.name || `page_${index + 1}`)}_page.dart`;
    const fileName = reserveUnique(baseFileName, seenFileNames);

    const builderState = coerceBuilderState(page.builderState, page.name || `Page ${index + 1}`);
    const builderStateBase64 = encodeBase64(JSON.stringify(builderState));

    return {
      id: page.id,
      name: page.name,
      route,
      slug,
      className,
      fileName,
      builderStateBase64,
      dynamicInputs: page.dynamicInputs ?? []
    };
  });
};

