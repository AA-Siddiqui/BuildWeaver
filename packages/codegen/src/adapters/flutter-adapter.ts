const buildFlutterFiles = (project: ProjectIR): GeneratedFile[] => {
  const pages = buildPageModels(project);

  console.info(`${LOG_PREFIX} Building Flutter project files`, {
    project: project.metadata.name,
    routes: pages.map((page) => page.route),
    pageCount: pages.length
  });

  const files: GeneratedFile[] = [
    { path: 'pubspec.yaml', contents: createPubspec(project) },
    { path: 'analysis_options.yaml', contents: createAnalysisOptions() },
    { path: '.gitignore', contents: createGitIgnore() },
    { path: '.env', contents: createEnvFile() },
    { path: '.env.example', contents: createEnvFile() },
    { path: '.metadata', contents: createMetadataFile() },
    { path: 'README.md', contents: createReadme(project, pages) },
    { path: 'lib/main.dart', contents: createMainFile(pages) },
    { path: 'lib/config/app_env.dart', contents: createAppEnvFile() },
    { path: 'lib/data/page_data_client.dart', contents: createPageDataClientFile() },
    { path: 'lib/models/generated_models.dart', contents: createGeneratedModelsFile() },
    {
      path: 'lib/generated/project_manifest.dart',
      contents: createProjectManifestFile(project, pages)
    },
    { path: 'lib/render/page_renderer.dart', contents: createPageRendererFile() }
  ];

  for (const page of pages) {
    files.push({
      path: `lib/pages/${page.fileName}`,
      contents: createPageFile(page)
    });
  }

  console.info(`${LOG_PREFIX} Prepared ${files.length} Flutter file(s)`);
  return files;
};

export const FlutterAdapter: CodegenAdapter = {
  name: 'flutter',
  target: 'flutter',
  async generate(ir) {
    console.info(`${LOG_PREFIX} Starting Flutter code generation for "${ir.metadata.name}"`);

    const project = normalizeProject(ir);
    const files = buildFlutterFiles(project);

    const bundle = createBundle('flutter', files, {
      irVersion: project.version,
      summary: `Flutter app for ${project.metadata.name}`,
      entryFile: 'lib/main.dart',
      metadata: {
        pages: project.pages.length,
        routeCount: project.pages.length
      }
    });

    console.info(`${LOG_PREFIX} Bundle created with ${bundle.files.length} file(s), id="${bundle.id}"`);
    return bundle;
  }
};
